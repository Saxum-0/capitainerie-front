<template>
  <form @submit.prevent="handleLogin" class="login-form">
    <div>
      <label>Email :</label>
      <input v-model="email" type="email" required />
    </div>
    <div>
      <label>Mot de passe :</label>
      <input v-model="password" type="password" required />
    </div>
    <button type="submit">Se connecter</button>
    <p v-if="error" class="error">{{ error }}</p>
  </form>
</template>

<script setup>
import { ref } from 'vue';
import { useRouter } from 'vue-router';
import { useUserStore } from '@/stores/user';
import api from '@/config/api';
import { useToast } from 'vue-toastification'; // üßÅ import du toast

const email = ref('');
const password = ref('');
const error = ref('');
const router = useRouter();
const userStore = useUserStore();
const toast = useToast(); // üßÅ instance du toast

const handleLogin = async () => {
  error.value = '';

  console.log("üîó API =", import.meta.env.VITE_API_URL);
  console.log("üì§ Donn√©es envoy√©es :", { email: email.value, password: password.value });

  try {
    const response = await api.post('/login', {
      email: email.value,
      password: password.value
    });

    userStore.setUser(response.data);
    toast.success('‚úÖ Connexion r√©ussie !');
    router.push('/dashboard');
  } catch (err) {
    console.error("‚ùå ERREUR API :", err);
    const msg = err.response?.data?.error || 'Erreur lors de la connexion';
    error.value = msg;
    toast.error(`‚ùå ${msg}`);
  }
};
</script>

<style scoped>
.login-form {
  margin-top: 2rem;
